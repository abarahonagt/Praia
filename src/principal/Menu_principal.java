    private void printRecord(JPanel panel){
        
        //creacion del trabajo de impresion
        PrinterJob printerJob = PrinterJob.getPrinterJob();
        
        //fijar nombre de impresion
        printerJob.setJobName("Praia | Factura FEL #0001 | 02/10/2020");
        
        printerJob.setPrintable(new Printable() {
            @Override
            public int print(Graphics graphics, PageFormat pageFormat, int pageIndex) throws PrinterException {
               //verifica el contenido
                if(pageIndex > 0){
                    return Printable.NO_SUCH_PAGE;   
                }
                //mapea el contenido grafico
                Graphics2D graphics2D = (Graphics2D)graphics;
                
                graphics2D.translate(pageFormat.getImageableX()*2, pageFormat.getImageableY()*2);
                
                //escala roosevelth
                graphics2D.scale(0.6, 0.6);
                
                //convertir a grafico el panel
                panel.paint(graphics2D);
                
                //retornar si existe pagina
                return Printable.PAGE_EXISTS;
            }
        });
        
        //mostrar cuadro de dialogo
        boolean returningResult = printerJob.printDialog();
        
        if(returningResult){
            try{
                //llamare al metodo imprimir dentro de printerJob
                printerJob.print();
            }catch(PrinterException printerException){
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error durante la impresion"+ printerException.getMessage());
            }
        }
    }

    
    
