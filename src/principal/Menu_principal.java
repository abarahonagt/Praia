    private void bt_eliminarFACActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_eliminarFACActionPerformed
        //Se declara una variable fila_sel que obtendra un origen por medio de la funcion getselectrow 
        //(o sea para cualquier animal que no entienda, obtendra la fila seleccionada :v) 
        int fila_sel = tb_Factura.getSelectedRow();
        
        if(fila_sel >=0){                           //Se valida la fila seleccionada con >=0 ya que 0 es la primera fila
            JOptionPane.showConfirmDialog(pn_0, "¿Estas seguro de eliminar las fila seleccionada?","Praia pregunta: ¿Estas seguro?",JOptionPane.OK_CANCEL_OPTION,QUESTION_MESSAGE);
            modelo4.removeRow(fila_sel);             // si se cumple se hace esta vaina
        }else{
            JOptionPane.showMessageDialog(pn_0,"Por favor selecciona una fila para continuar", 
                                                "Praia dice: Advertencia", WARNING_MESSAGE);   //sino se muestra este mensaje
        }
    }//GEN-LAST:event_bt_eliminarFACActionPerformed

